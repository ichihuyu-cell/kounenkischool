export type FlowerMoonType = keyof typeof flowerMoonTypes;

export function getTypeFromBirthData(birthDate: string, bloodType: string): FlowerMoonType {
  const date = new Date(birthDate);
  const year = date.getFullYear();
  const month = date.getMonth() + 1;
  const day = date.getDate();
  const moonAge = ((year - 2000) * 12.3685 + (month - 1) * 1 + day * 0.0295) % 29.53;
  
  let moonPhase: string;
  if (moonAge < 4.92) moonPhase = "new_moon";
  else if (moonAge < 9.84) moonPhase = "crescent";
  else if (moonAge < 14.77) moonPhase = "first_quarter";
  else if (moonAge < 19.69) moonPhase = "gibbous";
  else if (moonAge < 24.61) moonPhase = "full_moon";
  else moonPhase = "last_quarter";
  
  return `${moonPhase}_${bloodType}` as FlowerMoonType;
}